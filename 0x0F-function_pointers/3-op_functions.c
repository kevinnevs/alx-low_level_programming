#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - a funct that returns sum of a & b
 * @a: first int
 * @b: second int
 * Return: Always (0) Success
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - a funct that subtracts a & b
 * @a: first int
 * @b: second int
 * Return: Always (0) Success
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - a funct that returns product of a & b
 * @a: first int
 * @b: second int
 * Return: Always (0) Success
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - a funct that divides a & b
 * @a: first int
 * @b: second int
 * Return:Always (0) Success
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a funct that returns modulus of a & b
 * @a: first int
 * @b: second int
 * Return: Always (0) Success
 */
int op_mod(int a, int b)
{
	return (a % b);
}
